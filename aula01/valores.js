// Existem valores primitivos e não-primitivos no javascript

// Valores primitivos

let numero = 1 // o tipo 'number'

let string = 'uma string' // o tipo 'string' que representa uma cadeia de caracteres

let booleano = true // o tipo 'boolean' que representa verdadeiro ou falso

let vazioNull = null // o tipo null que presenta a ausência de um valor

let vazioUndefined = undefined // o tipo undefined que representa a não definição de valor, similar ao null

let bigInt = 42n // o tipo bigint

let nao_e_um_numero = NaN // o tipo NaN (not a number) que representa o resultado de uma operação matemática inválida, como 1 / 0


// Valores não primitivos

let meu_objeto = { nome: 'fernando', idade: 88, estaVivo: true } // o tipo de objetos, muito usado!

let meus_alunos = [ 'juliana', 'camila', 'jessica' ] // o tipo de lista (que veremos na aula de hoje)

